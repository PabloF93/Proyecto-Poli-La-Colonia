    private void refrescarAlumnos() {
        
        Alumno a1 = new Alumno();
        rs = a1.List();
        
        DefaultTableModel modelo = (DefaultTableModel) this.JTable_alumnos.getModel();
        modelo.setRowCount(0);
        int cantCols = modelo.getColumnCount();
        
        try {
            while(rs.next()) {
                Object[] fila = new Object[cantCols];
              
                fila[0] = rs.getObject(2);
                fila[1] = rs.getObject(4);
                fila[2] = rs.getObject(5);
                
                modelo.addRow(fila);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Alumno_Observaciones.class.getName()).log(Level.SEVERE, null, ex);
        }
       
        
        
    }
    
    private void refrescarObservaciones() {
        
        Alumno a1 = new Alumno();
        String dni = this.TxtField_dni.getText().trim();
        
        rs = a1.listObservations(dni);
        
        DefaultTableModel modelo = (DefaultTableModel) this.JTable_observaciones.getModel();
        modelo.setRowCount(0);
        int cantCols = modelo.getColumnCount();
        
        try {
            while(rs.next()) {
                Object[] fila = new Object[cantCols];
                
                fila[0] = rs.getObject(1);
                fila[1] = rs.getObject(2);
                fila[2] = rs.getObject(3);
                fila[3] = rs.getObject(4);
                fila[4] = rs.getObject(5);
                
                modelo.addRow(fila);
                
            }
        } catch (Exception e) {
        }
        
    }

