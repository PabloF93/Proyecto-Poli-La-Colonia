    private void Btn_editarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_editarActionPerformed
        Alumno a1 = new Alumno();
        Observacion o1 = new Observacion();
        int fila = this.JTable_observaciones.getSelectedRow();
        int observacionId = Integer.valueOf(this.JTable_observaciones.getValueAt(fila, 0).toString());
        
        o1.setId(observacionId);
        o1.setObservacion(this.TxtArea_observacion.getText().trim());
        o1.setUpdated_at(LocalDateTime.now());
        
        a1.updateObservation(o1);
        
        this.refrescarObservaciones();
    }//GEN-LAST:event_Btn_editarActionPerformed

    private void Btn_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_eliminarActionPerformed
        int resultado = JOptionPane.showConfirmDialog(this, "¿Seguro que quieres eliminar esta observación?", "Eliminar observación", JOptionPane.YES_NO_OPTION);
        if(resultado == JOptionPane.YES_NO_OPTION) {
            Alumno a1 = new Alumno();
            int fila = this.JTable_observaciones.getSelectedRow();
            int observacionId = Integer.valueOf(this.JTable_observaciones.getValueAt(fila, 0).toString());

            a1.deleteObservation(observacionId);

            this.refrescarObservaciones();
        }
        
        
    }//GEN-LAST:event_Btn_eliminarActionPerformed

    private void Btn_buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_buscarActionPerformed
        int cantRows = this.JTable_alumnos.getModel().getRowCount();
        int cantCols = this.JTable_alumnos.getModel().getColumnCount();
        String dni = this.TxtField_buscarDni.getText().trim();
        
        for(int i=0; i < cantRows; i++) {
            for(int j=0; j < cantCols; j++) {
                if(this.JTable_alumnos.getValueAt(i, j).toString().equalsIgnoreCase(dni)) {
                    this.JTable_alumnos.setRowSelectionInterval(i, i);
                }
            }
        }
                
        
        
    }//GEN-LAST:event_Btn_buscarActionPerformed

