    private void Btn_registrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_registrarActionPerformed
        Alumno a1 = new Alumno();
        Alumno a2 = new Alumno();
        Observacion o1 = new Observacion();
        String dni = this.TxtField_dni.getText().trim();
        o1.setObservacion(this.TxtArea_observacion.getText().trim());
        o1.setCreated_at(LocalDateTime.now());
        o1.setUpdated_at(LocalDateTime.now());
        a1 = a1.find(dni);
        
        a1.saveObservation(a1.getId(), o1);
        
        this.refrescarObservaciones();
    }//GEN-LAST:event_Btn_registrarActionPerformed

