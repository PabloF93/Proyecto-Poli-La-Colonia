    private void refrescarAlumnos() {
        Alumno a1 = new Alumno();
        
        DefaultTableModel modelo = (DefaultTableModel) this.JTable_alumnos.getModel();
        modelo.setRowCount(0);
        
        rs = a1.List();
        
        int cantCols = modelo.getColumnCount();
        
        try {
            while(rs.next()) {
                Object[] fila = new Object[cantCols];
                
                fila[0]= rs.getObject(2);
                fila[1]= rs.getObject(4);
                fila[2]= rs.getObject(5);
                
                modelo.addRow(fila);
                        
            }
        } catch (Exception e) {
        }
        
        
        
    }
    
    private void refrescarContactos() {
        
        int alumnoFila = this.JTable_alumnos.getSelectedRow();
        String alumnoDni = this.JTable_alumnos.getValueAt(alumnoFila, 0).toString();
        
        Alumno a1 = new Alumno();
        DefaultTableModel modelo = (DefaultTableModel) this.JTable_contactos.getModel();
        modelo.setRowCount(0);
        
        int cantCols = modelo.getColumnCount();
        
        rs = a1.listContacts(alumnoDni);
        
        try {
            while(rs.next()) {
                Object[] fila = new Object[cantCols];
                
                for (int i = 0; i < cantCols; i++) {
                    fila[i] = rs.getObject(i+1);
                }
                
                modelo.addRow(fila);
            }
        } catch (Exception e) {
        }
        
        
        
    }
    
