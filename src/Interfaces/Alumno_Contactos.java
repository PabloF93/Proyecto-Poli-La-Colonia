    private void Btn_buscarDniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_buscarDniActionPerformed
        
        if(this.TxtField_buscarDni.getText().trim() == "") {
            JOptionPane.showMessageDialog(this, "¡El campo no puede estar vacio!", "¡Error!", JOptionPane.ERROR_MESSAGE);
        } else {
            
            int cantRows = this.JTable_alumnos.getModel().getRowCount();
            int cantCols = this.JTable_alumnos.getModel().getColumnCount();
            String dni = this.TxtField_buscarDni.getText().trim();
            
            for (int i = 0; i < cantRows; i++) {
                for (int j = 0; j < cantCols; j++) {
                    
                    if(this.JTable_alumnos.getValueAt(i, j).toString().equalsIgnoreCase(dni)) {
                        this.JTable_alumnos.setRowSelectionInterval(i, i);
                    }
                    
                }
            }
                   
            
        }
        
    }//GEN-LAST:event_Btn_buscarDniActionPerformed

    private void Btn_registrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_registrarActionPerformed
        if(this.TxtField_nombre.getText().trim() == "" || this.TxtField_telefono.getText().trim() == "") {
            JOptionPane.showMessageDialog(this, "¡Los campos no pueden estar vacios!", "¡Error!", JOptionPane.ERROR_MESSAGE);
        } else {
            
            int fila = this.JTable_alumnos.getSelectedRow();
            String dni = this.JTable_alumnos.getValueAt(fila, 0).toString();
            
            Alumno a1 = new Alumno();
            a1 = a1.find(dni);
            
            Contacto c1 = new Contacto();
            c1.setNombreContacto(this.TxtField_nombre.getText().trim());
            c1.setTelefono(this.TxtField_telefono.getText().trim());
            c1.setVinculo(this.CBox_vinculo.getSelectedItem().toString());
            
            a1.saveContact(a1.getId(), c1);
            
            this.refrescarContactos();
            this.limpiarCampos();
            
        }
    }//GEN-LAST:event_Btn_registrarActionPerformed

