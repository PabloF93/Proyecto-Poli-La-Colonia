    private void Btn_editarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_editarActionPerformed
        int fila = this.JTable_contactos.getSelectedRow();
        
        Contacto c1 = new Contacto();
        c1.setId(Integer.parseInt(this.JTable_contactos.getValueAt(fila, 0).toString()));
        c1.setNombreContacto(this.TxtField_nombre.getText().trim());
        c1.setTelefono(this.TxtField_telefono.getText().trim());
        c1.setVinculo(this.CBox_vinculo.getSelectedItem().toString());
        
        Alumno a1 = new Alumno();
        a1.updateContact(c1);
        
        this.refrescarContactos();
        this.limpiarCampos();
        
    }//GEN-LAST:event_Btn_editarActionPerformed

    private void JTable_contactosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_JTable_contactosMouseClicked
        
        int fila = this.JTable_contactos.getSelectedRow();
        
        this.TxtField_nombre.setText(this.JTable_contactos.getValueAt(fila, 2).toString());
        this.TxtField_telefono.setText(this.JTable_contactos.getValueAt(fila, 3).toString());
        String vinculo = this.JTable_contactos.getValueAt(fila, 4).toString();
        
        for (int i = 0; i < this.CBox_vinculo.getItemCount(); i++) {
            if(this.CBox_vinculo.getItemAt(i).toString().equalsIgnoreCase(vinculo)) {
                this.CBox_vinculo.setSelectedIndex(i);
            }
            
        }
        
    }//GEN-LAST:event_JTable_contactosMouseClicked

    private void Btn_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_eliminarActionPerformed
        
        int resultado = JOptionPane.showConfirmDialog(this, "¿Estás seguro que quieres eliminar este contacto?", "Eliminar Contacto", JOptionPane.YES_NO_OPTION);
        if(resultado == JOptionPane.YES_NO_OPTION) {
            int fila = this.JTable_contactos.getSelectedRow();
            int contactoId = Integer.valueOf(this.JTable_contactos.getValueAt(fila, 0).toString());

            Alumno a1 = new Alumno();
            a1.deleteContact(contactoId);

            this.refrescarContactos();
            this.limpiarCampos();
        }
        
        
        
    }//GEN-LAST:event_Btn_eliminarActionPerformed

